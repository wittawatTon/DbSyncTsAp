BEGIN
  FOR i IN 1..10 LOOP
    EXECUTE IMMEDIATE '
      CREATE TABLE inventory_' || i || ' (
        id        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        field1    VARCHAR2(50),
        field2    VARCHAR2(50),
        field3    VARCHAR2(50),
        field4    VARCHAR2(50),
        field5    VARCHAR2(50),
        field6    NUMBER,
        field7    DATE,
        field8    VARCHAR2(50)
      )';
  END LOOP;
